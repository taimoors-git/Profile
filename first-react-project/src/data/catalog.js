function withProductDefaults(p) {
  return {
    ...p,
    stock: typeof p.stock === "number" ? p.stock : 25,
    description:
      typeof p.description === "string" ? p.description : "",
    badge: p.badge ?? null,
    compareAtValue:
      p.compareAtValue == null ? undefined : p.compareAtValue,
  };
}

/** Canonical seed used on first load and when admin resets the catalog. */
export const INITIAL_PRODUCTS = PRODUCTS.map(withProductDefaults);

export function formatPrice(value) {
  return `$${Math.round(value)}`;
}

export function getProductById(products, id) {
  return products.find((p) => p.id === id) ?? null;
}

export function filterProducts(products, { segment, category } = {}) {
  return products.filter(
    (p) =>
      (!segment || p.segment === segment) &&
      (!category || p.category === category)
  );
}

/** Normalize admin form / API payload into a catalog product row. */
export function normalizeProductInput(raw) {
  const id = String(raw.id ?? "").trim();
  const badgeRaw = String(raw.badge ?? "").trim();
  const compareRaw = raw.compareAtValue;
  const compareAtValue =
    compareRaw === "" || compareRaw == null
      ? undefined
      : Math.max(0, Number(compareRaw) || 0);

  return {
    id,
    segment: raw.segment,
    category: raw.category,
    title: String(raw.title ?? "").trim(),
    subtitle: String(raw.subtitle ?? "").trim(),
    description: String(raw.description ?? "").trim(),
    priceValue: Math.max(0, Number(raw.priceValue) || 0),
    compareAtValue,
    rating: Math.min(5, Math.max(0, Math.round(Number(raw.rating) || 0))),
    badge: badgeRaw || null,
    image: String(raw.image ?? "").trim(),
    stock: Math.max(0, Math.floor(Number(raw.stock) || 0)),
  };
}

export function getCategory(slug) {
  return CATEGORIES.find((c) => c.slug === slug) ?? null;
}

export function getSegment(id) {
  return SEGMENTS.find((s) => s.id === id) ?? null;
}

export function isValidShopPath(segment, categorySlug) {
  return Boolean(getSegment(segment) && getCategory(categorySlug));
}
